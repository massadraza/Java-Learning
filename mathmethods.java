class mathmethods {
	public static void main(String[] args){
		System.out.println(Math.abs(-392));
		System.out.println("----------");
		System.out.println(Math.ceil(7.2));
		System.out.println("----------");	
		System.out.println(Math.floor(7.9));
		System.out.println("----------"); 	
		System.out.println(Math.max(7.4, 5.2));
		System.out.println("----------"); 
		System.out.println(Math.min(3.2, 5.25));
		System.out.println("----------"); 
		System.out.println(Math.pow(5, 8));
		System.out.println("----------"); 
		System.out.println(Math.sqrt(948237));
	}
}
